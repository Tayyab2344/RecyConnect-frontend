  /// Get list of all cities (sorted alphabetically)
  static List<String> get cities {
    List<String> cityList = citiesAndAreas.keys.toList();
    cityList.sort();
    return cityList;
  }

  /// Get areas for a specific city
  static List<String> getAreasForCity(String city) {
    return citiesAndAreas[city] ?? [];
  }

  /// Search cities by query (case-insensitive)
  static List<String> searchCities(String query) {
    if (query.isEmpty) return cities;
    
    return cities
        .where((city) => city.toLowerCase().contains(query.toLowerCase()))
        .toList();
  }

  /// Search areas by query for a specific city (case-insensitive)
  static List<String> searchAreas(String city, String query) {
    List<String> areas = getAreasForCity(city);
    if (query.isEmpty) return areas;
    
    return areas
        .where((area) => area.toLowerCase().contains(query.toLowerCase()))
        .toList();
  }
  
  /// Get total number of cities
  static int get totalCities => citiesAndAreas.length;
  
  /// Get total number of areas across all cities
